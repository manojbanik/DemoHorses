
public class Horse
{
	
   private String name, color, birthYear;
	
	
   public void setName(String Name)
	{
	   name = Name;  
	}
   public void setColor(String Color)
	{
	   color = Color;  
	}
   public void setYear(String Year)
	{
	   birthYear = Year; 
	}
   public String getName()
   {
	   return name;
   }
   public String getColor()
   {
	   return color;
   }
   public String getYear()
   {
	   return birthYear;
   }   
}